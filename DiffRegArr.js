// Differnce Between Regular Functionn and Arrow Function

//              Regular Functions:

// 1)Defined using the function keyword.
// 2)this depends on the calling context.
// 3)Has its own arguments object.
// 4)Can be used as a constructor with new.
// 5)Function declarations are hoisted.
// 6)Requires return for returning values.
// 7)Suitable for object methods with proper this.

//             Arrow Function 

// 1)Uses concise => syntax.
// 2)Inherits this from the surrounding scope.
// 3)Does not have its own arguments object.
// 4)Cannot be used as a constructor.
// 5)Not hoisted; behaves like variables.
// 6)Supports implicit return for single expressions.
// 7)Not suitable for methods due to lexical this.
